// Helper function to get countries with search
window.getFilteredCountries = function(search) {
    if (!search) return window.COUNTRY_PHONE_CODES;
    const s = search.toLowerCase();
    return window.COUNTRY_PHONE_CODES.filter(c =>
        c.country.toLowerCase().includes(s) || c.code.includes(s)
    );
};

// Get country by ISO code
window.getCountryByIso = function(iso) {
    return window.COUNTRY_PHONE_CODES.find(c => c.iso === iso);
};

// Get country by phone code
window.getCountryByCode = function(code) {
    return window.COUNTRY_PHONE_CODES.find(c => c.code === code);
};
